// Array Methods
// POP and Push 
// Delete last value and value in last index of arrays

// POP
var Students = ["Ali", "Kamran", "Shoaib"]
Students.pop()
console.log(Students)

// PUSH
var Students = ["Ali", "Kamran", "Shoaib"]
Students.push("Ahmed")
console.log(Students)

// Shift and Unshift 
// Delete First value and value in First index of arrays

// SHIFT
var Students = ["Ali", "Kamran", "Shoaib"]
Students.shift()
console.log(Students)

// unshift
var Students = ["Ali", "Kamran", "Shoaib"]
Students.unshift("Ahmed")
console.log(Students)